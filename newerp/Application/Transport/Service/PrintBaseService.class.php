    function pdf2png($path){

        /**
         *测试人员谭 2018-06-21 16:45:50
         *说明: 这里我需要将 ebay_id 提取出来 放到url 里面 这样在 nginx 日志里面可以得到该死的请求时间
         */

        $ebay_id=0;
        if(preg_match("/(\d+)\.pdf/",$path,$arr)){
            $ebay_id=$arr[1];
        }

        $url='http://erpcnapi.com:8081/erpcnapi/pdf2img/API.php?test='.$ebay_id;

        // 有这个配置存在 就说明是仓库系统.仓库系统的话 就好办了 直接到这个数据表中取数据
        if(C('C_WH_ID')){
            $OrderType=new OrderTypeModel();
            $data=$OrderType->getImgUrl($ebay_id);
            if($data!=false){
                /**
                *测试人员谭 2018-08-09 19:16:41
                *说明:// 前期bug 导致
                */
                for($i=0;$i<count($data['data']);$i++){
                    $data['data'][$i]=str_replace('//opt/web/erpcnapi/erpcnapi/pdf2img','',$data['data'][$i]);
                }
                return $data;
            }
        }

        //TODO 打印的人 请将域名 帮到 hosts ： 218.17.38.218  erpcnapi.com
        $connection = curl_init();
        curl_setopt($connection, CURLOPT_URL, $url);
        curl_setopt($connection, CURLOPT_POST, 1);
        curl_setopt($connection, CURLOPT_RETURNTRANSFER,1);
        curl_setopt($connection, CURLOPT_POSTFIELDS, http_build_query(['path'=>$path]));
        curl_setopt($connection, CURLOPT_CONNECTTIMEOUT, 15);
        curl_setopt($connection, CURLOPT_TIMEOUT, 30);
        $response = curl_exec($connection);
        curl_close($connection);
        #echo '##'.$response.'##'.$path;
        return json_decode($response, true);
    }


    //俄速通要的一些东西
    function getZoneNum($ebay_postcode) {
        if ($ebay_postcode == '' || !is_numeric($ebay_postcode)) {
            return false;
        }
        $p1 = substr($ebay_postcode, 0, 1);
        $p2 = substr($ebay_postcode, 0, 2);
        $p3 = substr($ebay_postcode, 0, 3);
        if ($p1 <= 4) {
            return $p1;
        }

        if ($p2 == '60' || $p2 == '61' || $p2 == '62' || $p3 == 640 || $p3 == 641 || $p3 == 642) {
            return 4;
        }
        if ($p1 == 6) {
            return 6;
        }

        return false;
    }

